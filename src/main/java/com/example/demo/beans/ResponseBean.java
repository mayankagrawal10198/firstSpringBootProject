package com.example.demo.beans;

public class ResponseBean {
    private String id;
    private String message;

    public ResponseBean() {

    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }
}
